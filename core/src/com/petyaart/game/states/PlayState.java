    public static final float SPACE = 6.5f;
    private Cigarette mCigarette;
    private Ball mBall;
    private Vector3 touchPos;
    private Music music;

    private Array<Block> blocks;
    private int mMassNumber = 0;

    public PlayState(GameStateManager gsm) {
        super(gsm);

        mCamera.setToOrtho(false, SmokingGame.WIDTH / 2, SmokingGame.HEIGHT / 2);
        Gdx.gl.glClearColor(0f, 0f, 0f, 1);

        mCigarette = new Cigarette(SmokingGame.WIDTH / 4 - 84 / 2, 0);
        mBall = new Ball(SmokingGame.WIDTH / 4, 7);
        blocks = new Array<Block>();

        touchPos = new Vector3();

        for (int j = 0; j < 34; j++){
            for (int i = 0; i < 42; i++){
                if (massiv[mMassNumber] == 1){
                    blocks.add(new Block((float)i * SPACE, SmokingGame.HEIGHT / 2 - 5 - ((float)j * SPACE)));
                }
                mMassNumber++;
            }
        }

        music = Gdx.audio.newMusic(Gdx.files.internal("xxxtentacion.mp3"));
        music.setLooping(true);
        music.setVolume(0.1f);
        music.play();
    }

    @Override
    protected void handleInput() {
        if (Gdx.input.isTouched()){
            touchPos.set(Gdx.input.getX(), Gdx.input.getY(), 0);
            mCamera.unproject(touchPos);
            mCigarette.shift(touchPos.x - 84 / 2);
        }

        if(Gdx.input.isKeyPressed(Input.Keys.LEFT)) mCigarette.LeftOrRight(0f);
        if(Gdx.input.isKeyPressed(Input.Keys.RIGHT)) mCigarette.LeftOrRight(1f);


    }

    @Override
    public void update(float dt) {
        handleInput();
        mBall.update();
        mCigarette.update();
        if (mBall.getPosition().y < -5){
            gsm.set(new GameOver(gsm));
        }
        if (mBall.collides(mCigarette.getCigaretteBlock())){
            mBall.PlayerVector(MathUtils.random(-1f, 1f), MathUtils.random(0.3f, 1f));
        }
        for (Block mBlock : blocks ) {
            if (mBall.collides(mBlock.getBLockTrue()) && mBlock.isShow() == true){
                if (((mBlock.getPosition().y + 6 > (mBall.getPosition().y)) && (mBlock.getPosition().y < (mBall.getPosition().y + 5)) && ((mBlock.getPosition().x) < (mBall.getPosition().x + 2.5f))) && ((mBlock.getPosition().x + 6) > (mBall.getPosition().x + 2.5f))){
                    mBall.setPlayerMoveY(-1);
                    mBlock.setShow(false);
                    return;
                }
                else if (((mBlock.getPosition().x + 6) > (mBall.getPosition().x)) && ((mBlock.getPosition().x) < (mBall.getPosition().x)) && ((mBlock.getPosition().y < (mBall.getPosition().y + 2.5f))) && ((mBlock.getPosition().y + 6 > mBall.getPosition().y + 2.5f))){
                    mBall.setPlayerMoveX(-1);
                    mBlock.setShow(false);
                }
                else if (((mBlock.getPosition().x + 6) > (mBall.getPosition().x + 5)) && ((mBlock.getPosition().x) < (mBall.getPosition().x + 5)) && ((mBlock.getPosition().y < (mBall.getPosition().y + 2.5f))) && ((mBlock.getPosition().y + 6 > mBall.getPosition().y + 2.5f))){
                    mBall.setPlayerMoveX(-1);
                    mBlock.setShow(false);
                }

            }
        }
    }

    @Override
    public void render(SpriteBatch sb) {
        sb.setProjectionMatrix(mCamera.combined);
        sb.begin();
        sb.draw(mCigarette.getCigarette(), mCigarette.getPosition().x, mCigarette.getPosition().y);
        sb.draw(mBall.getBall(), mBall.getPosition().x, mBall.getPosition().y);
        for (Block mBlock : blocks ) {
            if (mBlock.isShow() == true){
                sb.draw(mBlock.getBlock(), mBlock.getPosition().x, mBlock.getPosition().y);
            }
        }
        sb.end();
    }

    @Override
    public void dispose() {
        mBall.dispose();
        for (Block mBlock: blocks){
            mBlock.dispose();
        }
        music.dispose();
    }

